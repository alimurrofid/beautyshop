    <script>
        function confirmVerif(id) {
            Swal.fire({
                title: 'Konfirmasi verifikasi',
                text: 'Yakin Verifikasi User!',
                icon: 'warning',
                showCancelButton: true,
                confirmButtonColor: '#3085d6',
                cancelButtonColor: '#d33',
                confirmButtonText: 'Ya, Verif!',
                cancelButtonText: 'Batal'
            }).then((result) => {
                if (result.isConfirmed) {
                    // Jika pengguna mengklik "Ya, Hapus!", kirimkan permintaan penghapusan ke server
                    document.getElementById('verif-form-' + id).submit();
                    Swal.fire(
                        'Diverifikasi!',
                        'User berhasil diverifikasi.',
                        'success'
                    )
                }
            });
        }
    </script>
